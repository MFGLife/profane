

document.getElementById('copyDogSymbol').addEventListener('click', function() {
    navigator.clipboard.writeText('🐕').then(function() {
        console.log('🐕 symbol copied to clipboard');
    }).catch(function(err) {
        console.error('Could not copy text: ', err);
    });
});

